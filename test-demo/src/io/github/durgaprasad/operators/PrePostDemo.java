package io.github.durgaprasad.operators;

public class PrePostDemo {
	public static void main(String args[]) {
	 int i =3;
	 i++;
	 // 4
	 System.out.println(i);
	 ++i;
	 //5
	 System.out.println(i);
	 // 6
	 System.out.println(++i);
	// 6
	 System.out.println(++i);
	 // 7
	 System.out.println(i);
	}
}
